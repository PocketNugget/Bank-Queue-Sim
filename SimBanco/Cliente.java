public class Cliente {
    private int id;
    private int horaLleg;

    public Cliente(int id, int horaLleg) {
        this.id = id;
        this.horaLleg = horaLleg;
    }

    public int getId() {
        return id;
    }

    public int getHoraLleg() {
        return horaLleg;
    }
}
